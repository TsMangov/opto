package bigproject.demo.model.service;

public class SupplierServiceModel {
    private String supplierName;
    private String city;
    private String country;
    private String address;
    private String contactPerson;
    private String phoneNumber;
    private String bankName;
    private String bic;
    private String iban;
    private String uid;
    private String vat;
    private String code;

    public SupplierServiceModel() {
    }

    public String getSupplierName() {
        return supplierName;
    }

    public SupplierServiceModel setSupplierName(String supplierName) {
        this.supplierName = supplierName;
        return this;
    }

    public String getCity() {
        return city;
    }

    public SupplierServiceModel setCity(String city) {
        this.city = city;
        return this;
    }

    public String getCountry() {
        return country;
    }

    public SupplierServiceModel setCountry(String country) {
        this.country = country;
        return this;
    }

    public String getAddress() {
        return address;
    }

    public SupplierServiceModel setAddress(String address) {
        this.address = address;
        return this;
    }

    public String getContactPerson() {
        return contactPerson;
    }

    public SupplierServiceModel setContactPerson(String contactPerson) {
        this.contactPerson = contactPerson;
        return this;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public SupplierServiceModel setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
        return this;
    }

    public String getBankName() {
        return bankName;
    }

    public SupplierServiceModel setBankName(String bankName) {
        this.bankName = bankName;
        return this;
    }

    public String getBic() {
        return bic;
    }

    public SupplierServiceModel setBic(String bic) {
        this.bic = bic;
        return this;
    }

    public String getIban() {
        return iban;
    }

    public SupplierServiceModel setIban(String iban) {
        this.iban = iban;
        return this;
    }

    public String getUid() {
        return uid;
    }

    public SupplierServiceModel setUid(String uid) {
        this.uid = uid;
        return this;
    }

    public String getVat() {
        return vat;
    }

    public SupplierServiceModel setVat(String vat) {
        this.vat = vat;
        return this;
    }

    public String getCode() {
        return code;
    }

    public SupplierServiceModel setCode(String code) {
        this.code = code;
        return this;
    }
}
